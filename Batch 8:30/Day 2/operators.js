//Operators

//Logical Operators
// && (AND) - returns true if both operands are true
// || (OR) - returns true if at least one operand is true
// ! (NOT) - returns true if the operand is false

//Comparison Operators
// == (Equal to) - returns true if operands are equal
// === (Strict equal to) - returns true if operands are equal and of the same type
// != (Not equal to) - returns true if operands are not equal
// !== (Strict not equal to) - returns true if operands are not equal or not of the same type
// > (Greater than) - returns true if left operand is greater than right operand
// < (Less than) - returns true if left operand is less than right operand
// >= (Greater than or equal to) - returns true if left operand is greater than or equal to right operand
// <= (Less than or equal to) - returns true if left operand is less than or equal to right operand

//Arithmetic Operators
// + (Addition) - adds two operands
// - (Subtraction) - subtracts right operand from left operand
// * (Multiplication) - multiplies two operands
// / (Division) - divides left operand by right operand
// % (Modulus) - returns the remainder of division of left operand by right operand
// ++ (Increment) - increases an integer value by one
// -- (Decrement) - decreases an integer value by one

//Assignment Operators
// = (Assignment) - assigns a value to a variable
// += (Add and assign) - adds right operand to left operand and assigns the result to left operand
// -= (Subtract and assign) - subtracts right operand from left operand and assigns the result to left operand
// *= (Multiply and assign) - multiplies left operand by right operand and assigns the result to left operand
// /= (Divide and assign) - divides left operand by right operand and assigns the result to left operand
// %= (Modulus and assign) - takes modulus using two operands and assigns the result to left operand

